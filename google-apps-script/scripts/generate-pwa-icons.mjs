/**
 * יוצר PNG base64 לאייקוני PWA מקובץ הלוגו ומעדכן Code.gs
 * הרצה: node scripts/generate-pwa-icons.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const gasDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = path.join(gasDir, 'assets', 'logo-yeshiva.png');

/** כחול כהה של מסגרת הלוגו */
const NAVY = { r: 6, g: 24, b: 38 };

/** זום – העיגול ממלא את כל האייקון */
const FILL_ZOOM = 1.38;

if (!fs.existsSync(sourcePath)) {
  console.error('חסר קובץ:', sourcePath);
  process.exit(1);
}

async function loadSharp() {
  try {
    return (await import('sharp')).default;
  } catch {
    const { execSync } = await import('child_process');
    execSync('npm install sharp --no-save', { cwd: gasDir, stdio: 'inherit' });
    return (await import('sharp')).default;
  }
}

function isJpegGrayBg(r, g, b) {
  return Math.abs(r - 203) < 14 && Math.abs(g - 203) < 14 && Math.abs(b - 203) < 14;
}

/** מחליף רקע אפור של ה-JPEG */
async function replaceGrayBackground(sharp, bg) {
  const { data, info } = await sharp(sourcePath).ensureAlpha().raw().toBuffer({ resolveWithObject: true });
  const { width, height, channels } = info;

  for (let i = 0; i < data.length; i += channels) {
    const r = data[i];
    const g = data[i + 1];
    const b = data[i + 2];
    if (isJpegGrayBg(r, g, b)) {
      data[i] = bg.r;
      data[i + 1] = bg.g;
      data[i + 2] = bg.b;
      if (channels === 4) data[i + 3] = 255;
    }
  }

  return sharp(data, { raw: { width, height, channels } }).png().toBuffer();
}

/** קובץ מקור רחב (1024×558) – חותכים ריבוע מרכזי סביב העיגול */
async function toCenterSquare(sharp, cleaned) {
  const meta = await sharp(cleaned).metadata();
  const side = Math.min(meta.width || 1, meta.height || 1);
  const left = Math.max(0, Math.floor(((meta.width || 1) - side) / 2));
  const top = Math.max(0, Math.floor(((meta.height || 1) - side) / 2));
  return sharp(cleaned)
    .extract({ left, top, width: side, height: side })
    .png()
    .toBuffer();
}

async function buildIconPng(sharp, size, bg, cleaned) {
  const square = await toCenterSquare(sharp, cleaned);
  const inner = Math.round(size * FILL_ZOOM);
  const crop = Math.max(0, Math.floor((inner - size) / 2));

  return sharp(square)
    .resize(inner, inner, { fit: 'cover', position: 'centre' })
    .extract({ left: crop, top: crop, width: size, height: size })
    .flatten({ background: bg })
    .removeAlpha()
    .png({ compressionLevel: 6, force: true })
    .toBuffer();
}

const sharp = await loadSharp();
const bg = NAVY;

const cleaned = await replaceGrayBackground(sharp, bg);
await sharp(cleaned).toFile(path.join(gasDir, 'assets', 'logo-cleaned.png'));

const sizes = { ICON_B64_180: 180, ICON_B64_192: 192, ICON_B64_512: 512 };
const b64 = {};

for (const [key, size] of Object.entries(sizes)) {
  const buf = await buildIconPng(sharp, size, bg, cleaned);
  b64[key] = buf.toString('base64');
  console.log(key, size, buf.length, 'bytes');
}

const previewPath = path.join(gasDir, 'assets', 'icon-preview-512.png');
await sharp(Buffer.from(b64.ICON_B64_512, 'base64')).toFile(previewPath);

const meta = await sharp(previewPath).metadata();
const corner = await sharp(previewPath).extract({ left: 8, top: 8, width: 1, height: 1 }).raw().toBuffer();
console.log('alpha:', meta.hasAlpha, 'פינה:', Array.from(corner));

const codePath = path.join(gasDir, 'Code.gs');
let code = fs.readFileSync(codePath, 'utf8');

const block =
  '// --- PWA icon PNG (base64), generated by scripts/generate-pwa-icons.mjs ---\n' +
  Object.entries(b64)
    .map(([k, v]) => `var ${k} = '${v}';`)
    .join('\n') +
  '\n// --- end PWA icons ---\n';

if (code.includes('// --- PWA icon PNG')) {
  code = code.replace(/\/\/ --- PWA icon PNG[\s\S]*?\/\/ --- end PWA icons ---\n/, block);
} else {
  code = code.replace('function getWebAppUrl_()', block + '\nfunction getWebAppUrl_()');
}

fs.writeFileSync(codePath, code, 'utf8');
console.log('עודכן Code.gs – הרץ setupPwaIconOnDrive + New version');
